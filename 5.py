m = ['круг', 0.25, 'квадрат', 'треугольник', 15, 'круг', 'овал', '10']

# Список известных названий фигур (можно дополнить при необходимости)
figure_names = ['круг', 'квадрат', 'треугольник', 'овал']

# Используем цикл while для безопасного удаления элементов во время итерации.
# Удаляем элементы, которых нет в списке figure_names
i = 0
while i < len(m):
    # Проверяем, является ли текущий элемент строкой и содержится ли он
    # в списке figure_names.
    # Элемент '10' хоть и является строкой, но не является названием фигуры.
    if isinstance(m[i], str) and m[i] in figure_names:
        i += 1
    else:
        # Если элемент не подходит под условия, удаляем его
        m.remove(m[i]) # remove() удаляет первое вхождение указанного значения

print(m)
